    private void jTextField1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jTextField1ActionPerformed
        
        String code = "";
        
        try {
            code = client.controllaCarta(jTextField1.getText());
        
            if(code.equals(Client.CARD_NOT_FOUND)) {
            
                JOptionPane.showMessageDialog(this.getContentPane(), "Tessera non trovata", "ERRORE", JOptionPane.ERROR_MESSAGE);
                
            } else {
                
                jTextField1.setEditable(false);
                jLabel5.setText("Punti tessera: " + code);
                canProceed = true;
                omaggi = client.visualizzaOmaggi(jTextField1.getText());
                jTextArea1.setText(omaggi);
                
                if(!omaggi.equals(Client.NO_OMAGGIO)) {
                    
                    jTextField2.setEnabled(true);
                }
            }
        } catch (IOException | InterruptedException ex) {
            Logger.getLogger(FidelityGUI.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTextField1ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        try {
            if(!canProceed)    
            
                JOptionPane.showMessageDialog(this.getContentPane(), "Impossibile procedere. Inserire un codice corretto", "Attenzione", JOptionPane.WARNING_MESSAGE);
            else {
            
                String omaggio = Client.OMAGGIO_NOT_FOUND;
                
                if(jTextField2.getText().equals("")) { //se non ha scritto niente

                    JOptionPane.showMessageDialog(this.getContentPane(), "Non hai inserito nessun omaggio. Procederemo con l'acquisto", "Informazione", JOptionPane.INFORMATION_MESSAGE);
                    this.setVisible(false);
                    new ScontrinoGUI(client).setVisible(true);
                }
                else {
                    omaggio = client.scegliOmaggio(jTextField2.getText());
                    
                    if(omaggio.equals(Client.OMAGGIO_NOT_FOUND)) {
                
                        jTextField2.setText("");
                        JOptionPane.showMessageDialog(this.getContentPane(), Client.OMAGGIO_NOT_FOUND, "ERRORE", JOptionPane.ERROR_MESSAGE);
                
                    } else {
                        jTextField2.setEditable(false);
                        JOptionPane.showMessageDialog(this.getContentPane(), "Omaggio scelto correttamente");
                        this.setVisible(false);
                        new ScontrinoGUI(client).setVisible(true);
                    }
                }
                
            }
        } catch (IOException | InterruptedException ex) {
            Logger.getLogger(FidelityGUI.class.getName()).log(Level.SEVERE, null, ex);
    }
        
    }//GEN-LAST:event_jButton1ActionPerformed

